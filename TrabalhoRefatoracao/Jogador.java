public class Jogador {
private String nome;
private String Camisa;
private int idade;



public String getNome() {
	return nome;
}

public void setNome(String nome) {
	this.nome = nome;
}

public String getCamisa() {
	return Camisa;
}

public void setCamisa(String Camisa) {
	this.Camisa = Camisa;
}

public int getIdade() {
	return idade;
}

public void setIdade(int idade) {
	this.idade = idade;
}

}
